package ast;

/**
 * Classe enumerativa che rappresenta gli operatori nel nodo delle espressioni binarie
 * @author Matteo Schirinzi 20035542
 */
public enum LangOper {
	/**
     * Somma
     */
	PLUS,
	/**
     * Sottrazione
     */
	MINUS,
	/**
     * Moltiplicazione
     */
	TIMES,
	/**
     * Divisione
     */
	DIV
}
